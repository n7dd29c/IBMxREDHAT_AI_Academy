import pandas as pd

data = [
    ['삼성', '1000', '2000'],
    ['현대', '1100', '3000'],
    ['엘지', '2000', '500'],
    ['아모레', '3500', '6000'],
    ['네이버', '100', '1500']
]

index = ['031', '059', '033', '045', '023']
columns = ['종목명', '시가', '종가']

df = pd.DataFrame(data=data, index=index, columns=columns)
print(df)
#      종목명    시가    종가
# 031   삼성    1000    2000
# 059   현대    1100    3000
# 033   엘지    2000     500
# 045  아모레   3500    6000
# 023  네이버    100    1500

print('===============================================================================================')
print('============================= 시가가 1000원 이상인 행을 모두 출력 =============================')
print('===============================================================================================')

cond1 = df['시가'] >= '1100'
print(cond1)
# 031    False
# 059     True
# 033     True
# 045     True
# 023    False
# Name: 시가, dtype: bool

print(df[cond1])
#      종목명    시가    종가
# 059   현대  1100  3000
# 033   엘지  2000   500
# 045  아모레  3500  6000

print(df[df['시가'] >= '1100'])
#      종목명    시가    종가
# 059   현대  1100  3000
# 033   엘지  2000   500
# 045  아모레  3500  6000

print(df.loc[df['시가'] >= '1100'])
#      종목명    시가    종가
# 059   현대  1100  3000
# 033   엘지  2000   500
# 045  아모레  3500  6000

print('\n===============================================================================================')
print('========================= 시가가 1000원 이상인 행에서 종가만 모두 출력 ========================')
print('===============================================================================================')

df3 = df[df['시가'] >= '1100']['종가']
# df3 = df.loc[df['시가'] >= '1100']['종가']
print(df3)